package info.denoo.par4j;

public class Par4JIoException extends RuntimeException {

	public Par4JIoException() {
		super();
	}

	public Par4JIoException(String message, Throwable cause) {
		super(message, cause);
	}

	public Par4JIoException(String message) {
		super(message);
	}

	public Par4JIoException(Throwable cause) {
		super(cause);
	}

	
}
